<h4>
    Penjelasan Dashboard Aplikasi
</h4>
<p>
    Pada halaman beranda aplikasi ini, anda akan disajikan oleh informasi/dashboard aplikasi yang memuat informasi jumlah data 
    yang mungkin membantu anda mengolah data yang ada. Pada halaman tersebut anda dapat memperoleh informasi jumlah 
    transaksi pendapatan dan penjualan secara aktual, jumlah inventory dan pengguna aplikasi dan lain-lain.
</p>

<p>
    <b>Menampilkan Grafik</b><br>
    Pada halaman ini anda dapat menampilkan data penjualan, pembelian dan rekapitulasi laba/rugi berupa tampilan 
    grafik sehingga mudah dipahami. Untuk menampilkan data grafik tersebut anda bisa melakukan langkah-langka 
    sebagai berikut:
    <ul>
        <li>
            Anda dapat mengklik tombol 'Grafik' yang ada pada bagian atas kanan halaman.
        </li>
        <li>
            Pilih kategori data yang ingin ditampilkan, pada select option anda bisa memilih penjualan, pembelian atau laba/rugi.
        </li>
        <li>
            Masukan tahun data yang ingin ditampilkan pada form tahun.
        </li>
        <li>
            Klik pada tombol tampilkan untuk memulai menampilkan grafik.
        </li>
    </ul>
    <img src="images/bantuan/tampilkan_grapik.png" alt="Grapik Penjualan" width="100%">
</p>
<p>
    <b>Menampilkan Data Barang Hampir Habis</b><br>
    Fitur ini berfungsi membantu petugas pengelola inventory untuk mengetahui item barang apa saja 
    yang sudah mulai mau habis pada nilai jumlah tertentu. Adapun cara penggunaannya sebagai berikut
    <ul>
        <li>
            Pilih tombol 'Barang Hampir Habis' pada bagian atas kanan halaman.
        </li>
        <li>
            isi batas data dengan jumlah data barang yang ingin ditampilkan,
        </li>
        <li>
            Pilih kategori barang yang ingin ditampilkan, pada select option anda bisa memilih semua untuk menampilkan semua kategori.
        </li>
        <li>
            Pada kolom stok minimal isi dengan jumlah stok minimal barang yang ingin ditampilkan.
        </li>
        <li>
            Klik pada tombol 'Lihat' untuk menampilkan data pada pop up halaman aplikasi, 
            atau klik pada tombol 'HTML' untuk menampilkan data barang pada halaman HTML tersendiri, dan tombol 'Excel' untuk 
            menyimpan data ke dalam file excel.
        </li>
    </ul>
    <img src="images/bantuan/barang_hampir_habis.png" alt="Barang Hampir Habis" width="100%">
</p>
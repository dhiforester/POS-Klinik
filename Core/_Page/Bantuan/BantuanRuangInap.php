<h4>
    Kelola Ruang Rawat Inap
</h4>
<p>
    Petugas perlu mengelola data ketersediaan ruang inap untuk mempermudah proses pendaftaran pasien rawat inap sehingga 
    informasi ketersediaan dapat berlangsung secara aktual. pada proses pengelolaan ruang rawat ini terdiri dari tambah data ruang inap, 
    edit/update ruang inap dan hapus ruang inap. Adapun uraian/rincian pasien yang menempati ruangan tersebut akan tersaji secara otomatis 
    setelah proses pendaftaran.
</p>
<p>
    <b>Tambah Data Ruang Inap</b><br>
    <img src="images/bantuan/tambah_ruang_inap.jpg" alt="Tambah Data Ruang inap" width="100%">
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menambahkan data ruang rawat inap:
    <ul>
        <li>
            Masuk ke halaman <i>Ruang Inap</i> dengan memilih menu 'Ruang Inap' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Tambah' yang ada pada bagian atas halaman.
        </li>
        <li>
            Isi form nama ruangan, kelas ruangan, kuota untuk laki-laki/Kuota (L), kuota untuk perempuan/Kuota (P) dan kuota untuk ruangan campur (LP) .
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan data ruangan inap tersebut.
        </li>
    </ul>
</p>
<p>
    <b>Edit Data Ruang Inap</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk merubah data Ruang Inap:<br>
    <img src="images/bantuan/edit_ruang_inap.jpg" alt="Edit Ruang Inap" width="100%">

    <ul>
        <li>
            Masuk ke halaman ruang inap dengan memilih menu 'Ruang Inap' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Edit' yang ada pada bagian kanan tabel berwarna hijau.
        </li>
        <li>
            Isi form ruang inap dengan lengkap.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan data.
        </li>
    </ul>
</p>
<p>
    <b>Hapus Data Ruang Inap</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menghapus data Ruang Inap:<br>
    <img src="images/bantuan/hapus_ruang_inap.jpg" alt="Hapus Data Ruang Inap" width="100%">

    <ul>
        <li>
            Masuk ke halaman ruang inap dengan memilih menu 'Ruang inap' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Hapus Data' yang ada pada bagian tabel kanan berwarna merah.
        </li>
        <li>
            Lakukan konfirmasi hapus data ruang inap dengan memilih tombol 'Ya'.
        </li>
    </ul>
</p>
<p>
    <b>Menampilkan Data Pasien Pada Ruangan Rawat Inap</b><br>
    Ketika petugas ingin mengetahui pasien yang sedang dirawat pada ruangan tertentu, anda bisa melihat data aktual 
    pasien yang dirawat tersebut. Berikut ini tahapan yang bisa anda lakukan<br>
    <img src="images/bantuan/pasien_ruang_inap.jpg" alt="Jumlah Pasien Ruang Inap" width="100%">
    <ul>
        <li>
            Pada abagian tabel pasien data ruang rawat inap, anda bisa melihat jumlah pasien yang dirawat waktu itu.
        </li>
        <li>
            Pilih (click) pada tombol jumlah pasien tersebut.
        </li>
    </ul>
    <img src="images/bantuan/data_pasien_ruang_inap.jpg" alt="List Data Pasien Ruang Inap" width="100%">
    <ul>
        <li>
            Anda bisa melihat uraian data pasien yang dirawat.
        </li>
        <li>
            data yang ditampilkan pada pop up tersebut adalah pasien yang pada saat itu berstatus checkin (Belum dipulangkan)
        </li>
    </ul>
</p>
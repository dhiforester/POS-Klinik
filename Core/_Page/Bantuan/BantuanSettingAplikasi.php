<h4>
    Setting Aplikasi
</h4>
<p>
    Untuk memulai menggunakan aplikasi ini, Anda harus mengkonfigurasi setting aplikasi terlebih dahulu 
    untuk menyesuaikan dengan pengaturan sistem dan aturan mode bisnis yang digunakan.
</p>
<p>
    Untuk melakukan konfigurasi setting aplikasi, Anda dapat melakukan hal berikut:
</p>
<p>
    <ul>
        <li>
            Pada bagian 'Sidebar' pilih 'Setting' untuk melakukan konfigurasi setting aplikasi.
        </li>
        <li>
            Isi parameter pada form yang disediakan.
        </li>
        <li>
            Pada bagian nama perusahaan, masukan nama perusahaan yang akan digunakan dalam aplikasi.
            Tidak lupa memasukan alamat dan kontak perusahaan sebagai informasi tambahan yang akan bermanfaat.
        </li>
        <li>
            Pada kolom 'Base URL' isikan nama URL/Link Utama aplikasi. Apabila anda menggunakannya pada server lokal dengan 
            nama aplikasi POS maka anda bisa mengisikan 'http://localhost/POS'
        </li>
        <li>
            Apabila anda menggunakan server lokal dan ingin menggunakan fitur cetak secara direct, silahkan isi host printer dan 
            nama printer. Secara standar pada kolom host biasa diisi dengan 'localhost' dan isikan juga nama printer yang digunakan 
            pada settingan windows printer.
        </li>
        <li>
            Pada bagian logo, pilih logo yang akan digunakan dalam aplikasi. Logo ini akan digunakan juga untuk tampilan 
            di nota dan kartu pasien kedepannya.
        </li>
        <li>
            Klik pada tombol 'Simpan'  untuk menyimpan setting aplikasi.
        </li>
    </ul>
</p>
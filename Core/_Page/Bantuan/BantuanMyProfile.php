<h4>My Profile</h4>
<p>
    Setiap pengguna aplikasi yang sudah login akan memiliki akun yang berisi informasi personal yang bisa diubah.
    Pada halaman ini pengguna bisa melakukan perubahan data pribadi seperti nama lengkap, 
    kontak email dan merubah password akses masuk. 
</p>
<img src="images/bantuan/profile.png" alt="Tab Profil Pengguna" width="100%">
<p>
    Untuk masuk ke halaman profile, pengguna bisa menggunakan menu <i>Sidebar</i> atau menggunakan tombol 
    profile pada menu atas <i>Navbar</i> halaman aplikasi. Pada halaman profile tersebut terdaapt beberapa fitur 
    seperti melihat data pribadi, melakukan perubahan data pribadi, melakukan perubahan password akses masuk, 
    melihat modul/fitur apa saja yang bisa diakses oleh pengguna. Selain itu pengguna juga bisa mengatur 
    ukuran dan tampilan untuk cetak kartu pasien, label obat dan nota transaksi. 
</p>
<p>
    <b>Edit Profile</b><br>
    <ul>
        <li>
            Untuk merubah profile data pribadi pengguna, pilih tab 'Detail Profile', kemudian klik pada tombol 'Edit profile' 
            yang ada pada bagian bawah halaman.
        </li>
        <li>
            Isi nama lengkap, kontak dan email yang valid. 
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan.
        </li>
    </ul>
    <img src="images/bantuan/edit_profil.png" alt="Edit Profil Pengguna" width="100%">
</p>
<p>
    <b>Ubah Password</b><br>
    Untuk memudahkan mengingat akun pengguna, pengguna bisa mengubah password akses masuknya.
    Berikut ini adalah langkah-langkah untuk merubah password akses masuk pengguna:
    <ul>
        <li>
            Masih pada tab 'Detail Profile', klik pada tombol 'Ubah Password' yang ada pada bagian bawah halaman.
        </li>
        <li>
            Isi password baru dan ulangi password tersebut pada form ulangi password.
        </li>
        <li>
            Klik 'Simpan' untuk menyimpan perubahan.
        </li>
    </ul>
    <img src="images/bantuan/ubah_password.png" alt="Ubah Password" width="100%">
</p>
<p>
    <b>Setting Kartu Pasien</b><br>
    Pengguna yang memiliki hak akses ke halaman pasien bisa melakukan percetakan kartu berobat pasien. 
    Untuk melakukan perubahan pada pengaturan ukuran dan tampilan dapat dilakukan pada tab 'Setting Kartu Pasien'.
</p>
<ul>
    <li>
        Klik pada tab 'Kartu Pasien' untuk melakukan perubahan pengaturan kartu pasien.
    </li>
    <li>
        Isi parameter ukuran kartu pasien yang akan dicetak.
    </li>
    <li>
        Isi juga mode tampilan yang disediakan.
    </li>
    <li>
        Klik 'Simpan' untuk menyimpan perubahan pada setting kartu pasien.
    </li>
</ul>

<p>
    <b>Setting Label Obat</b><br>
    Pengguna yang memiliki hak akses ke halaman obat bisa melakukan percetakan label obat.
    Untuk melakukan perubahan pada pengaturan ukuran dan tampilan dapat dilakukan pada tab 'Setting Label Obat'.
</p>
<p>
    <ul>
        <li>
            Klik pada tab 'Label Obat' untuk melakukan perubahan pengaturan label obat.
        </li>
        <li>
            Isi parameter ukuran label obat yang akan dicetak.
        </li>
        <li>
            Isi juga mode tampilan yang disediakan.
        </li>
        <li>
            Klik 'Simpan' untuk menyimpan perubahan pada setting label obat.
        </li>
    </ul>
</p>

<p>
    <b>Setting Nota Transaksi</b><br>
    Pengguna yang memiliki hak akses ke halaman transaksi bisa melakukan percetakan nota transaksi.
    Untuk melakukan perubahan pada pengaturan ukuran dan tampilan dapat dilakukan pada tab 'Setting Nota Transaksi'.
</p>
<p>
    <ul>
        <li>
            Klik pada tab 'Nota' untuk melakukan perubahan pengaturan nota transaksi.
        </li>
        <li>
            Isi parameter ukuran nota transaksi yang akan dicetak.
        </li>
        <li>
            Isi juga mode tampilan yang disediakan.
        </li>
        <li>
            Klik 'Simpan' untuk menyimpan perubahan pada setting nota transaksi.
        </li>
    </ul>
</p>

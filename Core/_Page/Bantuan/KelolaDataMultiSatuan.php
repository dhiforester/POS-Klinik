<h4>
    Kelola Data Batch
</h4>
<p>
    Beberapa pengelola inventory mengharapkan adanya pengaturan untuk jenis inventory yang memiliki beragam satuan dan harga.
    Untuk mempermudah user pengelola inventory maka dengan adanya data multi harga dan satuan dapat mempersingkat waktu pengelolaan data 
    inventory secara parsial.
</p>
<p>
    <b>Input Data Multi Harga & Satuan</b><br>
    <img src="images/bantuan/multi_satuan.jpg" alt="Input Multi Harga dan Satuan" width="100%">
    <ul>
        <li>Masuk ke menu <b>Inventory</b>.</li>
        <li>Pada bagian tabel, pilih salah satu item inventory yang akan anda tambahkan data multi harga dan satuannya nya.</li>
        <li>Sistem akan menampilkan <i>Popup</i> detail inventory.</li>
        <li>pada bagian bawah pilih tombol option kemudian pilih pada opsi <i>Multi Harga & Satuan</i></li>
        <li>
            Pada kolom kosong yang tersedia, masukan barcode (Apabila Ada) atau kode inventory multi satuan dan harga yang ingin diinput.<br>
            <img src="images/bantuan/form_multi_satuan.jpg" alt="Form Multi Harga dan Satuan" width="100%">
        </li>
        <li>Masukan jenis satuannya, jumlah isi dalam kemasan dan harga.</li>
        <li>Terakhir pilih tombol <i>Add</i> untuk memulai menyimpan data multi harga dan satuan tersebut.</li>
    </ul>
    <b>Edit Data Multi Harga & Satuan</b>
    <ul>
        <li>Masih pada halaman yang sama, pada bagian tabel 'Multi Harga & Satuan' tersebut anda bisa langsung melakukan perubahan data apabila diinginkan</li>
        <li>Pilih tombol edit dengan logo pencil berwarna kuning pada bagian kanan tabel</li>
        <li>
            Apabila berhasil maka sistem akan menampilkan pemberitahuan.<br>
            <img src="images/bantuan/edit_multi_satuan.jpg" alt="Hapus Batch" width="100%">
        </li>
        <li>
            Tutup <i>Popup</i> pemberitahuan tersebut dan data yang akan diubah akan secara otomatis di posting pada kolom yang sama seperti pada proses tambah data pada tahapan sebelumnya
            <img src="images/bantuan/edit_multi_satuan2.jpg" alt="Hapus Batch" width="100%">
        </li>
        <li>Klik pada tombol 'Simpan' untuk menyimpan perubahan data.</li>
    </ul>
    <b>Hapus Data Multi Harga & Satuan</b>
    <ul>
        <li>Masih pada halaman yang sama, pada bagian tabel tersebut anda bisa langsung menghapus data multi satuan</li>
        <li>Pilih tombol hapus berwarna merah pada baris tabel multi satuan dan harga</li>
        <li>
            Lakukan konfirmasi proses hapus data dengan memilih tombol <b>Hapus</b>.<br>
            <img src="images/bantuan/hapus_multi_satuan.jpg" alt="Hapus Multi Satuan" width="100%">
        </li>
        <li>Apabila berhasil maka data akan langsung terhapus dari tabel</li>
    </ul>
</p>
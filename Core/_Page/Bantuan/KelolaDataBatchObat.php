<h4>
    Kelola Data Batch
</h4>
<p>
    Nomor Batch terkadang disertakan pada beberapa item barang yang termasuk dalam golongan penting dan beresiko adanya penukaran 
    retur barang tidak sah. Oleh sebab itu beberapa pengelola melakukan pencatatan data batc untuk menghindari proses retur 
    yang disrtai dengan penukaran barang yang bukan berasal dari pemilik asli. Berikut ini adalah beberapa 
    tahapan yang bisa anda lakukan untuk mengelola data batch tersebut.
</p>
<p>
    <b>Input Data Batch</b><br>
    <img src="images/bantuan/input_batch.jpg" alt="Input batch" width="100%">
    <ul>
        <li>Masuk ke menu <b>Inventory</b>.</li>
        <li>Pada bagian tabel, pilih salah satu item inventory yang akan anda tambahkan data batch nya.</li>
        <li>Sistem akan menampilkan <i>Popup</i> detail inventory.</li>
        <li>pada bagian bawah pilih tombol option kemudian pilih pada opsi <i>Show Batch</i></li>
        <li>Input nomor batch yang ada pada produk inventory pada kolom <i>Kode Batch</i> dan tanggal expired pada kolom <i>Expired</i></li>
        <li>Pilih tombol 'add' untuk memulai proses penyimpanan data pada database.</li>
        <li>Apabila proses berhasil maka pada tabel data batch akan muncul data yang barusan anda input.</li>
    </ul>
    <b>Hapus Data Batch</b>
    <ul>
        <li>Masih pada halaman yang sama, pada bagian tabel batch tersebut anda bisa langsung menghapus nomor batch</li>
        <li>Pilih tombol hapus berwarna merah pada baris tabel nomor batch</li>
        <li>
            Apabila berhasil maka data nomor batch akan terhapus langsung dari tabel.<br>
            <img src="images/bantuan/hapus_batch.jpg" alt="Hapus Batch" width="100%">
        </li>
        <li>Isi form edit inventory sesuai perubahan yang ingin anda lakukan</li>
        <li>Klik pada tombol 'Simpan' untuk menyimpan perubahan data.</li>
    </ul>
    <b>Pencarian Nomor Batch</b>
    <ul>
        <li>Untuk melakukan pencarian nomor batch yang sudah anda input sebelumnya, anda bisa melakukan pencarian pada halaman utama inventory.</li>
        <li>Klik tombol 'Batch' Pada bagian atas halaman utama inventory.</li>
        <li>
           Sistem akan menampilkan fom isian pencarian, masukan nomor batch barang pada kolom pencarian tersebut.
        </li>
        <li>
            Pilih tombol merah pada bagian kanan kolom untuk memulai pencarian.<br>
            <img src="images/bantuan/cari_batch.jpg" alt="Cari Batch" width="100%">
        </li>
    </ul>
</p>
<h4>
    Kelola Data master pasien
</h4>
<p>
    Setiap pasien yang berkunjung ke fasilitas kesehatan, perlu dilakukan pendaftaran. Salah satunya adalah 
    mendata identitas utama pasien pada data master pasien. Tahapan yang biasa dilakukan adalah pasien yang datang 
    dilakukan pendataan apakah pasien sudah terdata (pernah berkunjung sebelumnya) atau belum terdata (pasien baru).
    Untuk pasien baru maka pada fitur data pasien ini perlu dilakukan input data pasien baru terlebih dahulu.
    Berikut alur kerja yang bisa diimplementasikan.
</p>
<p>
    <b>Tambah Data Pasien</b><br>
    <img src="images/bantuan/tambah_pasien.jpg" alt="Tambah Data Pasien" width="100%">
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menambahkan data pasien:
    <ul>
        <li>
            Masuk ke halaman <i>Pasien</i> dengan memilih menu 'Pasien' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Tambah' yang ada pada bagian atas halaman.
        </li>
        <li>
            Isi form NIK, nama lengkap, gender, tanggal lahir, kontak, status dan alamat.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan data pasien tersebut.
        </li>
    </ul>
</p>
<p>
    <b>Edit Data Pasien</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk merubah/Edit data Pasien:<br>
    <img src="images/bantuan/edit_pasien.jpg" alt="Edit Pasien" width="100%">

    <ul>
        <li>
            Masuk ke halaman pasien dengan memilih menu 'Pasien' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Edit' dengan logo pensil yang ada pada bagian kanan tabel berwarna hijau.
        </li>
        <li>
            Isi form pasien dengan lengkap. Pada bagian ini anda bisa melakukan perubahan data NIK, nama lengkap, gender, tanggal lahir, kontak, status dan alamat.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan data.
        </li>
    </ul>
</p>
<p>
    <b>Hapus Data Pasien</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menghapus data Pasien:<br>
    <img src="images/bantuan/hapus_pasien.jpg" alt="Hapus Data Pasien" width="100%">

    <ul>
        <li>
            Masuk ke halaman pasien dengan memilih menu 'Pasien' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Hapus Pasien' yang ada pada bagian tabel kanan berwarna merah.
        </li>
        <li>
            Lakukan konfirmasi hapus data Pasien dengan memilih tombol 'Ya'.
        </li>
    </ul>
</p>
<p>
    <b>Menampilkan Informasi Rincian pasien</b><br>
    Anda bisa mengetahui informasi rincian pasien termasuk histori kunjungan yang pernah dilakukan<br>
    <img src="images/bantuan/detail_pasien.jpg" alt="Jumlah Pasien Ruang Inap" width="100%">
    <ul>
        <li>
            Pada bagian tabel pasien, klik pada bagian kolom nomor rekam medis.
        </li>
        <li>
            Sistem akan menampilkan halaman popup rincian informasi pasien tersbut.
        </li>
    </ul>
    <img src="images/bantuan/cetak_kartu_pasien.jpg" alt="Cetak Kartu Pasien" width="100%">
    <ul>
        <li>
            Pada rincian pasien anda bisa melakukan cetak kartu dengan 2 cara, yaitu melakukan cetak melalui file PDF dan halaman HTML
        </li>
        <li>
            Pada bagian data kunjungan pasien, pada gambar terlihat masih kosong dengan keterangan "Data Kunjungan Pasien Belum Ada!!".
            Data Kunjungan akan dijelaskan pada modul selanjutnya.
        </li>
    </ul>
</p>
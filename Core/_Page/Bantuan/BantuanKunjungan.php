<h4>
    Pendaftaran Kunjungan pasien
</h4>
<p>
    Setelah mendaftarkan pasien pada halaman modul pasien, langkah selanjutnya adalah mendaftarkan pasien 
    pada database kunjungan. Proses pendaftaran ini dilakukan bertepatan pada saat pasien berkunjung. 
    Berikut ini adalah tahapan-tahapan yang bisa anda lakukan untuk melakukan pendaftaran kunjungan pasien.
</p>
<p>
    <b>Tambah Data Kunjungan</b><br>
    <img src="images/bantuan/tambah_kunjungan.jpg" alt="Tambah Data Kunjungan" width="100%">
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menambahkan data kunjungan pasien:
    <ul>
        <li>
            Masuk ke halaman <i>Kunjungan/Berobat</i> dengan memilih menu 'Kunjungan/Berobat' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Tambah' yang ada pada bagian atas halaman.
        </li>
        <li>
            Pilih pasien yang melakukan kunjungan tersebut, apabila data pasien belum terdaftar silahkan lakukan pendaftaran 
            pasien baru pada tahapan/modul sebelumnya.<br>
            <img src="images/bantuan/pilih_kunjungan_pasien.jpg" alt="Pilih Data Pasien" width="100%">
        </li>
        <li>
            Isi tanggal kunjungan, tujuan kunjungan, keluhan penyakit, nama dokter, ruang inap (jika pasien rawat inap), pilih diagnosa awal pasien, 
            status pendaftaran pasien, tanggal pulang/meninggal dan jam pulang/meninggal (Hanya apabila pasien meninggal atau pulang).<br>
            <img src="images/bantuan/form_kunjungan_pasien.jpg" alt="Form Tambah Kunjungan Pasien" width="100%">
        </li>
        <li>
            Apabila data dokter belum ada, anda bisa melakukan pengelolaan data dokter pada fitur/modul sebelumnya.
        </li>
        <li>
            Begitu juga dengan data ruangan, apabila belum ada anda bisa melakukan pengelolaan ruangan pada modul sebelumnya.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan data kunjungan pasien tersebut.
        </li>
    </ul>
</p>
<p>
    <b>Edit Data Kunjungan Pasien</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk merubah/Edit data Kunjungan Pasien:<br>
    <img src="images/bantuan/edit_kunjungan_pasien.jpg" alt="Edit Kunjungan Pasien" width="100%">

    <ul>
        <li>
            Masuk ke halaman kunjungan/berobat dengan memilih menu 'kunjungan/berobat' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Edit' dengan logo pensil yang ada pada bagian kanan tabel berwarna hijau.
        </li>
        <li>
            Isi form kunjungan pasien dengan lengkap. Pada bagian ini anda bisa melakukan perubahan
            tanggal kunjungan, tujuan kunjungan, keluhan penyakit, nama dokter, ruang inap (jika pasien rawat inap), pilih diagnosa awal pasien, 
            status pendaftaran pasien, tanggal pulang/meninggal dan jam pulang/meninggal (Hanya apabila pasien meninggal atau pulang)
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan data.
        </li>
    </ul>
</p>
<p>
    <b>Hapus Data KunjunganPasien</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menghapus data Kunjungan pasien:<br>
    <img src="images/bantuan/hapus_kunjungan_pasien.jpg" alt="Hapus Data Kunjungan Pasien" width="100%">

    <ul>
        <li>
            Masuk ke halaman Kunjungan/Berobat  dengan memilih menu 'Kunjungan/Berobat' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Hapus Kunjungan' yang ada pada bagian tabel kanan berwarna merah.
        </li>
        <li>
            Lakukan konfirmasi hapus data Pasien dengan memilih tombol 'Ya'.
        </li>
    </ul>
</p>
<p>
    <b>Menampilkan Informasi Rincian Kunjungan</b><br>
    Anda bisa mengetahui informasi rincian kunjungan<br>
    <img src="images/bantuan/detal_kunjungan_pasien.jpg" alt="Rincian Kunjungan Pasien" width="100%">
    <ul>
        <li>
            Pada bagian tabel kunjungan, klik pada bagian kolom nomor rekam medis.
        </li>
        <li>
            Sistem akan menampilkan halaman popup rincian informasi kunjungan tersbut.
        </li>
    </ul>
</p>
<h4>
    Kelola Data Supplier
</h4>
<p>
    Setiap transaksi pembelian perlu referensi sumber supplier, data supplier cukup penting untuk dikelola untuk memenuhi informasi 
    lanjutan dari transaksi. Berikut ini tahapan yang bisa dilakukan dalam kelola data supplier.
</p>
<p>
    <b>Tambah Data Supplier</b><br>
    <img src="images/bantuan/Tambah_supplier.jpg" alt="Tambah Data Supplier" width="100%">
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menambahkan data supplier:
    <ul>
        <li>
            Masuk ke halaman <i>Supplier</i> dengan memilih menu 'Supplier' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Tambah' yang ada pada bagian atas halaman.
        </li>
        <li>
            Isi data nama supplier, nomor identitas, perusahaan dan alaamt.<br>
            <img src="images/bantuan/form_tambah_supplier.jpg" alt="Pilih Data Pasien" width="100%">
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan supplier tersebut.
        </li>
    </ul>
</p>
<p>
    <b>Edit Data Supplier</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk merubah/Edit data Supplier:<br>
    <img src="images/bantuan/form_edit_supplier.jpg" alt="Edit Supplier" width="100%">

    <ul>
        <li>
            Masuk ke halaman Supplier dengan memilih menu 'Supplier' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Edit' dengan logo pensil yang ada pada bagian kanan tabel berwarna hijau.
        </li>
        <li>
            Isi form perubahan data Supplier tersebut.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan data.
        </li>
    </ul>
</p>
<p>
    <b>Hapus Data Supplier</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menghapus data supplier:<br>
    <img src="images/bantuan/form_hapus_supplier.jpg" alt="Hapus Data Supplier" width="100%">

    <ul>
        <li>
            Masuk ke halaman supplier  dengan memilih menu 'supplier' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Hapus Supplier' yang ada pada bagian tabel kanan berwarna merah.
        </li>
        <li>
            Lakukan konfirmasi hapus data supplier dengan memilih tombol 'Ya'.
        </li>
    </ul>
</p>
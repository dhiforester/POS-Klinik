<h4>
    Data Dokter & Poliklinik
</h4>
<p>
    Untuk mempermudah pengguna dalam menghubungkan data dokter & poliklinik pada pelayanan kunjungan pasien, maka 
    terlebih dahulu dilakukan pengelolaan data dokter & poliklinik tersebut. Pengguna aplikasi dapat menambahkan, merubah (edit) dan menghapus 
    data/referensi dokter dan poliklinik yang akan digunakan pada pelayanan kunjungan pasien.
</p>
<p>
    <b>Tambah Data Dokter & Poliklinik</b><br>
    <img src="images/bantuan/tambah_dokter.png" alt="Tambah Data Dokter Dan Poliklinik" width="100%">
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menambahkan data dokter & poliklinik:
    <ul>
        <li>
            Masuk ke halaman Dokter & Poliklinik dengan memilih menu 'Dokter & Poliklinik' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Tambah' yang ada pada bagian atas halaman.
        </li>
        <li>
            Isi form nama dokter, nama poliklinik dan status data dengan lengkap.
        </li>
        <li>
            Status digunakan untuk mengendalikan data dokter & poliklinik pada halaman pendaftaran kunjungan tanpa harus menghapus data dokter & poliklinik.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan data dokter & poliklinik.
        </li>
    </ul>
</p>
<p>
    <b>Edit Data Dokter & Poliklinik</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk merubah data dokter & poliklinik:<br>
    <img src="images/bantuan/edit_dokter.png" alt="Edit Data Dokter & Poliklinik" width="100%">

    <ul>
        <li>
            Masuk ke halaman Dokter & Poliklinik dengan memilih menu 'Dokter & Poliklinik' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Edit Data Koter & Poliklinik' yang ada pada bagian tabel data dokter & poliklinik.
        </li>
        <li>
            Isi form nama dokter, nama poliklinik dan status data dengan lengkap.
        </li>
        <li>
            Status digunakan untuk mengendalikan data dokter & poliklinik pada halaman pendaftaran kunjungan tanpa harus menghapus data dokter & poliklinik.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan perubahan data dokter & poliklinik.
        </li>
    </ul>
</p>
<p>
    <b>Hapus Data Dokter & Poliklinik</b><br>
    Berikut ini langkah-langkah yang bisa anda lakukan untuk menghapus data dokter & poliklinik:<br>
    <img src="images/bantuan/hapus_dokter.png" alt="Hapus Data Dokter & Poliklinik" width="100%">

    <ul>
        <li>
            Masuk ke halaman Dokter & Poliklinik dengan memilih menu 'Dokter & Poliklinik' pada bagian <i>Sidebar</i> Aplikasi.
        </li>
        <li>
            Klik pada tombol 'Hapus Data Koter & Poliklinik' yang ada pada bagian tabel data dokter & poliklinik.
        </li>
        <li>
            Lakukan konfirmasi hapus data dokter dan poliklinik dengan memilih tombol 'Ya'.
        </li>
    </ul>
</p>
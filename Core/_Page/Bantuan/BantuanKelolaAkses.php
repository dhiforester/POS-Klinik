<h4>
    Kelola Data Akses
</h4>
<p>
    Hak akses dapat melakukan pengaturan dan pengelola pengguna secara dinamis.
    Pada fitur kelola akses ini memungkinkan admin aplikasi melakukan kontrol terhadap akses pengguna. 
</p>

<p>
    <b>Tambah/Buat Akses Pengguna Baru</b><br>
    
    Pertama kali anda harus menambahkan data pengguna terlebih dahulu agar karyawan anda bisa melakukan login 
    pada aplikasi. Berikut ini adalah tahanan yang harus anda lakukan:
</p>
<p>
    <ul>
        <li>
            Klik tombol 'Tambah User' pada bagian atas halaman.
        </li>
        <li>
            Isi data pengguna pada form yang sudah disediakan. Isikan nama lengkap pengguna, nomor kontak, email,
            nama akses group sesuai yang diinginkan, masukan password dan status akses.
        </li>
        <li>
            Akses group digunakan untuk mengidentifikasi hak akses pengguna.
        </li>
        <li>
            Status digunakan untuk mengendalikan akses pengguna tanpa harus menghapus data pengguna.
        </li>
        <li>
            Klik tombol 'Simpan' untuk menyimpan data pengguna.
        </li>
    </ul>
    <img src="images/bantuan/tambah_akses.png" alt="Tambah Akses" width="100%"><br>
</p>
<p>
    <b>Edit Data Pengguna</b><br>
    Admin yang memiliki akses ke halaman kelola data akses dapat melakukan perubahan data akun pengguna lainnya melalui 
    fitur edit data akses ini. Berikut ini adalah tahanan yang harus anda lakukan:
    <ul>
        <li>
            Klik tombol 'Edit Akses' pada baris data pengguna yang ingin diubah.
        </li>
        <li>
            Isi data pengguna pada form yang sudah disediakan. Isikan nama lengkap pengguna, nomor kontak, email,
            nama akses group sesuai yang diinginkan, masukan password dan status akses.
        </li>
        <li>
            Akses group digunakan untuk mengidentifikasi hak akses pengguna.
        </li>
        <li>
            Klik 'Simpan' untuk menyimpan perubahan data pengguna.
        </li>
    </ul>
    <img src="images/bantuan/edit_akses.png" alt="Tambah Akses" width="100%"><br>
</p>
<p>
    <b>Hapus Data Pengguna</b><br>
    Admin yang memiliki akses ke halaman kelola data akses dapat menghapus data akun pengguna lainnya melalui 
    fitur hapus data akses ini. Berikut ini adalah tahanan yang harus anda lakukan:
    <ul>
        <li>
            Klik tombol 'Hapus Akses' pada baris data pengguna yang ingin diubah.
        </li>
        <li>
            Konfirmasi bahwa anda benar-benar ingin menghapus data tersebut.
        </li>
        <li>
            Klik pada tombol 'Hapus' pada bagian bawah <i>pop up</i>.
        </li>
    </ul>
    <img src="images/bantuan/hapus_akses.png" alt="Tambah Akses" width="100%"><br>
</p>
<p>
    <b>Pengaturan Aksesibilitas Pengguna</b><br>
    Setiap pengguna dapat ditentukan hak akses yang berbeda berdasarkan akses group. Pada tabel data akses anda bisa 
    memilih (click) pada nama aksesnya untuk melihat detail aksesnya. Sistem akan menampilkan data list ijin akses 
    pada masing-masing fitur yang tersedia. Pilih select option 'Yes' untuk memberikan ijin halaman yang dipilih kepada 
    akses group.
</p>

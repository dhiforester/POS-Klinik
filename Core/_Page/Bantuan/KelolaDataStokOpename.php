<h4>
    Stock Opename Inventory
</h4>
<p>
    Dalam periode tertentu, pengelola inventory biasanya akan melakukan pengecekan jumlah stok barang yang dibandingkan 
    dengan jumlah yang tercatat. Kegiatan ini bertujuan untuk menyamakan antara nilai pada manifestasi perusahaan 
    dengan stock nyata. Pada sistem ini anda bisa melakukan pencatatan stock nyata sekaligus mencatat perubahan stock 
    yang berlangsung.
</p>
<p>
    <b>Input Data Stock Opename</b><br>
    Berikut ini adalah langkah-langkah melakukan pencatatan stock opename 
    <img src="images/bantuan/stock_opename.png" alt="Stock Opename" width="100%">
    <ul>
        <li>Masuk ke menu <b>Inventory</b>.</li>
        <li>Pada bagian atas tabel, pilih <b>Stok Opename</b>.</li>
        <li>
            Pada <i>Popup</i> stock opename yang muncul, pada bagian atas terdapat form isian tanggal 
            periode waktu berlangsungnnya kegiatan <i>stock opename</i>
        </li>
        <li>Setelah mengisi periode waktu klik pada tombol terapkan.</li>
        <li>
            Klik pada kolom kode, pilih data inventory yang akan diinputkan nilai stock nya.
        </li>
        <li>
            Masukan nilai stock nyata dan keterangan apabila ada dan kemudian klik pada tombol <i>Add</i>.<br>
            <img src="images/bantuan/input_stock_opename.jpg" alt="Form Stock Opename" width="100%">
        </li>
        <li>Apabila proses berhasil maka data hasil stock opename tersebut akan tersimpan langsung pada tabel.</li>
    </ul>
    <b>Edit Data Stock Opename</b>
    <ul>
        <li>
            Masih pada halaman dan <i>Pop Up</i> yang sama, pada bagian baris tabel stock opename yang sudah anda input. 
            Anda bisa melakukan perubahan pada data tersebut. 
        </li>
        <li>Pilih tombol <i>Edit Stock Opename</i> pada baris data dengan tombol berlogo pencil berwarna kuning</li>
        <li>
            Data Edit akan secara otomatis mengisi kolom yang ada di atasnya.
        </li>
        <li>
            Isi perubahan data anda pada form tersebut kemudian klik pada tombol <i>Update</i>.<br>
            <img src="images/bantuan/edit_stock_openmae.jpg" alt="Form Edit Stock Opename" width="100%">
        </li>
        <li>Isi form edit inventory sesuai perubahan yang ingin anda lakukan</li>
        <li>Klik pada tombol 'Update' untuk menyimpan perubahan data.</li>
    </ul>
    <b>Hapus Data Stock Opename</b><br>
    <img src="images/bantuan/hapus_stock_openmae.jpg" alt="Form Edit Stock Opename" width="100%">
    <ul>
        <li>Masih pada halaman yang sama, dengan <i>Popup</i> yang sama pula, pada bagian baris data klik tombol hapus.</li>
        <li>Tombol hapus tersebut terdapat pada baris data tabel berwarna merah.</li>
        <li>
            Ketika anda memilih tombol ini, maka sistem akan langsung menghapus data tersebut.
        </li>
    </ul>
</p>
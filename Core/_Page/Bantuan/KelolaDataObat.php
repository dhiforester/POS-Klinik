<h4>
    Kelola Data Inventory
</h4>
<p>
    Pengelolaan data inventory meliputi aspek validitas informasi nama/merek dagang inventory yang bersangkutan, satuan jual, 
    stok yang tersedia, dan harga. Masing-masing komponen didukung oleh fitur yang dirancang mempermudah pengelolaan baik 
    penulisan data maupun aksesibilitas informasi. Setiap fitur pada halaman data inventory dijelaskan sebagai berikut:
</p>
<p>
    <b>Input Data Inventory</b><br>
    <img src="images/bantuan/tambah_inventory.jpg" alt="Tambah Inventory" width="100%">
    <ul>
        <li>Masuk ke menu <b>Inventory</b>.</li>
        <li>Pada bagian atas tabel, pilih <b>Tambah</b>.</li>
        <li>
            Sistem akan menampilkan form isian. Anda hanya perlu melakukan pengisian data inventory sesuai informasi yang ada.<br>
            <img src="images/bantuan/form_tambah_inventory.jpg" alt="Form Tamabah Inventory" width="100%">
        </li>
        <li>Pastikan anda mengisi form tersebut dengan benar. Apabila terdapat kesalahan maka sistem akan menampilkan pesan</li>
        <li>Pilih tombol simpan untuk memulai proses penyimpanan data pada database.</li>
    </ul>
    <b>Edit Data Inventory</b>
    <ul>
        <li>Pada tabel inventory, pilih salah satu item inventory yang akan di edit.</li>
        <li>Sistem akan menampilkan <i>Popup</i> halaman <i>Detail Inventory</i>.</li>
        <li>
            Pada bagian bawah halaman tersebut terdapat tombol <b>Option</b>, klik tombol tersebut 
            kemudian akan muncul beberapa pilihan.
        </li>
        <li>
            Pilih opsi Edit untuk memulai melakukan perubahan data.<br>
            <img src="images/bantuan/edit_inventory.jpg" alt="Form Tamabah Inventory" width="100%">
        </li>
        <li>Isi form edit inventory sesuai perubahan yang ingin anda lakukan</li>
        <li>Klik pada tombol 'Simpan' untuk menyimpan perubahan data.</li>
    </ul>
    <b>Hapus Data Inventory</b>
    <ul>
        <li>Pada tabel inventory, pilih salah satu item inventory yang akan di hapus.</li>
        <li>Sistem akan menampilkan <i>Popup</i> halaman <i>Detail Inventory</i>.</li>
        <li>
            Pada bagian bawah halaman tersebut terdapat tombol <b>Hapus</b> berwarna merah, klik tombol tersebut 
            kemudian akan muncul popup baru untuk melakukan konfirmasi.
        </li>
        <li>
            Pilih Ya untuk melakukan proses penghapusan data.<br>
            <img src="images/bantuan/hapus_inventory.jpg" alt="Form Tamabah Inventory" width="100%">
        </li>
    </ul>
</p>